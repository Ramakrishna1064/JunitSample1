package com.example.demo.service;

public class Math {
	
	public Math() {
		
	}

	/**
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public int add(int a,int b) {
		return a+b;
	}
	
	
	/**
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public int sub(int a,int b) {
		return a-b;
	}
	
	/**
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public int div(int a,int b) {
		return a/b;
	}
	
	/**
	 * 
	 * @return
	 */
	public int primeNumber() {
		return 5;
	}
}
